# dict func
# .keys()
user1 = {'name' : '스펜서', 'college' : '프로그래머스', 'year' : 20, 'lang' : 'Python'}
print(user1.keys())

# .values()
user1 = {'name' : '스펜서', 'college' : '프로그래머스', 'year' : 20, 'lang' : 'Python'}
print(user1.values())

# .items()
user1 = {'name' : '스펜서', 'college' : '프로그래머스', 'year' : 20, 'lang' : 'Python'}
print(user1.items())

# .get()
user1 = {'name' : '스펜서', 'college' : '프로그래머스', 'year' : 20, 'lang' : 'Python'}
print(user1.get('name'))
print(user1.get('country'))
